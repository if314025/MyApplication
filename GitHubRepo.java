public class GitHubRepo {
    private int id;
    private String name;

    public GitHubRepo(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public GitHubRepo() {
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
